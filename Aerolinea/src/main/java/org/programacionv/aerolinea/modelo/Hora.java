package org.programacionv.aerolinea.modelo;

public class Hora {

	private int id;
	private Dia dia;
	public Hora() {
		
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Dia getDia() {
		return dia;
	}
	public void setDia(Dia dia) {
		this.dia = dia;
	}
	
	
}
